package springofintegration

import (
	"reflect"
	"testing"
)

func TestChristmasTree(t *testing.T) {
	var actual []string
	tests := []struct {
		num    int
		height int
		x      []string
	}{
		{1, 3, []string{
			"    *",
			"    *",
			"   ***",
			"  *****",
			" *******",
			"*********",
			"   ***"},
		},
		{2, 4, []string{
			"      *",
			"      *",
			"     ***",
			"    *****",
			"   *******",
			"  *********",
			" ***********",
			"   *******",
			"  *********",
			" ***********",
			"*************",
			"    *****",
			"    *****"},
		},
		{4, 8, []string{
			"            *",
			"            *",
			"           ***",
			"          *****",
			"         *******",
			"        *********",
			"       ***********",
			"      *************",
			"     ***************",
			"    *****************",
			"   *******************",
			"         *******",
			"        *********",
			"       ***********",
			"      *************",
			"     ***************",
			"    *****************",
			"   *******************",
			"  *********************",
			"        *********",
			"       ***********",
			"      *************",
			"     ***************",
			"    *****************",
			"   *******************",
			"  *********************",
			" ***********************",
			"       ***********",
			"      *************",
			"     ***************",
			"    *****************",
			"   *******************",
			"  *********************",
			" ***********************",
			"*************************", // 25
			"        *********",
			"        *********",
			"        *********",
			"        *********"},
		},
	}

	for _, test := range tests {
		actual = christmasTree(test.num, test.height)
		if !reflect.DeepEqual(actual, test.x) {
			t.Errorf("christmasTree(%d, %d) == %q, want %q", test.num, test.height, actual, test.x)
		}
	}
}
